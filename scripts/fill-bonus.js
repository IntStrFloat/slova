function canBuild(word, letters) {
  const p = new Map();
  for (const l of letters) p.set(l, (p.get(l) || 0) + 1);
  for (const ch of word) {
    const n = p.get(ch) || 0;
    if (!n) return false;
    p.set(ch, n - 1);
  }
  return true;
}

function main() {
  const args = process.argv.slice(2);
  const get = (flag, def) => {
    const i = args.indexOf(flag);
    return i >= 0 ? args[i + 1] : def;
  };
  const world = get('--world', 'world1');
  const maxBonus = parseInt(get('--max', '10'), 10);

  const dictPath = path.join(__dirname, '..', 'assets', 'dict', 'ru.json');
  const base = JSON.parse(fs.readFileSync(dictPath, 'utf8'));
  // Нормализуем (ё→е, только кириллица) и отбрасываем мусор/латиницу.
  const dict = [...new Set([...base, ...EXTRA].map(normalize))]
    .filter((w) => w.length >= 3 && w.length <= 8)
    .sort();
  fs.writeFileSync(dictPath, JSON.stringify(dict));

  const lvlPath = path.join(__dirname, '..', 'assets', 'levels', `${world}.json`);
  const pack = JSON.parse(fs.readFileSync(lvlPath, 'utf8'));

  let total = 0;
  let withBonus = 0;
  for (const lvl of pack.levels) {
    const answers = new Set(lvl.answers.map((a) => a.word));
    const bonus = dict
      .filter(
        (w) =>
          w.length >= 3 &&
          w.length <= lvl.letters.length &&
          !answers.has(w) &&
          canBuild(w, lvl.letters),
      )
      .sort((a, b) => b.length - a.length || a.localeCompare(b))
      .slice(0, maxBonus);
    lvl.bonusPool = bonus;
    total += bonus.length;
    if (bonus.length) withBonus++;
  }
  fs.writeFileSync(lvlPath, JSON.stringify(pack));
  console.log(
    `Словарь: ${dict.length} слов. Уровней с бонусами: ${withBonus}/${pack.levels.length}, всего бонусов: ${total}.`,
  );
}

main();
